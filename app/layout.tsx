import { dehydrate } from '@tanstack/react-query';
import './colorfull.css';

import type { Metadata } from 'next';
import { Inter } from 'next/font/google';
import { PropsWithChildren } from 'react';

import { QueryKey } from '~/common/const/querykey';
import { Providers } from '~/components/provider/application';
import { getQueryClient } from '~/lib/query/client';
import { Permission, Role } from '~/modules/users/users.type';

export const font = Inter({
  subsets: ['latin'],
});

export const metadata: Metadata = {
  title: 'Next Application',
  description: 'Generated by create next app',
};

declare module 'next-auth' {
  interface UserRole {
    id: number;
    name: string;
  }

  interface UserPermission {
    id: number;
    key: string;
    name: string;
  }

  interface User {
    id: number;
    roles: UserRole[];
    permissions: UserPermission[];
  }

  interface Session {
    user: {
      id: number;
      name?: string | null;
      email?: string | null;
      image?: string | null;
      roles?: UserRole[];
      permissions?: UserPermission[];
    };
  }
}

declare module 'next-auth/jwt' {
  interface JWT {
    id: number;
    customerId: number;
    roles?: Role[];
    permissions?: Permission[];
  }
}

declare module '@tanstack/react-query' {
  interface Register {
    mutationMeta: {
      invalidateQueries: QueryKey[];
    };
  }
}

export default function RootLayout({ children }: Readonly<PropsWithChildren>) {
  const queryClient = getQueryClient();
  return (
    <html lang="en" suppressHydrationWarning>
      <body className={`${font.className} antialiased`}>
        <Providers dehydratedState={dehydrate(queryClient)}>{children}</Providers>
      </body>
    </html>
  );
}
